create table Room (
	roomId INT IDENTITY(1,1) PRIMARY KEY,
	address VARCHAR(50),
	area VARCHAR(50),
	description VARCHAR(50),
	cost int,
	status VARCHAR(50),
	note VARCHAR(50),
	roomTypeId int FOREIGN KEY REFERENCES RoomType(roomTypeId)
);
create table RoomType (
	roomTypeId INT IDENTITY(1,1) PRIMARY KEY,
	roomTypeName VARCHAR(50), 
);


