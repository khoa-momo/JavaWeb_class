    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        NewJDialog dl = new NewJDialog(this, true);
        dl.setVisible(true);

        String s = "";

        if (dl.countword(jTextArea1.getText()) > 4) {
            s = s.concat("Teacher: " + dl.name());
            s = s.concat("\nthe document too more “toi” words");
            s = s.concat("\n" + dl.email());
            jTextArea1.setText(s);
        } else if (dl.countword(jTextArea1.getText()) < 2) {
            s = s.concat("Teacher: " + dl.name());
            s = s.concat("\nthe document a little “toi” word");
            s = s.concat("\n" + dl.email());
            jTextArea1.setText(s);
        } else {
            s = s.concat("Teacher: " + dl.name());
            s = s.concat("\nthe document ok");
            s = s.concat("\n" + dl.email());
            jTextArea1.setText(s);
        }

    }//GEN-LAST:event_jMenuItem1ActionPerformed

