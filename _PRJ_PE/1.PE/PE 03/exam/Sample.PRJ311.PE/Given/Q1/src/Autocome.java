    public void execute() {
        isRun = true;
    }

    public void stopRun() {
        isRun = false;
    }

    class TextThread extends Thread {

        @Override
        public void run() {
            try {
                while (true) {
                    if (isRun) {
                        if (jLabel1.getForeground() == Color.BLUE) {
                            jLabel1.setForeground(Color.RED);
                        } else {
                            jLabel1.setForeground(Color.BLUE);
                        }
                        Thread.sleep(jSlider1.getValue());
                    }
                }
            } catch (Exception e) {
                System.out.println(e);
            }
        }

    }

    public Autocome() {
        initComponents();
        jLabel1.setForeground(Color.BLUE);
        TextThread t = new TextThread();
        t.start();
    }

