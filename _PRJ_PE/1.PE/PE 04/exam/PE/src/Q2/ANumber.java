    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        // TODO add your handling code here:
        Calculate cl = new Calculate(this, true);
        cl.setVisible(true);

        // Lấy ra một list số đã nhập ở TextField
        String textNumber = jTextField1.getText();
        String[] s = textNumber.split("\\s+");
        List<Integer> listnumber = new ArrayList<>();
        for (String x : s) {
            int number = Integer.parseInt(x);
            listnumber.add(number);
        }

        jTextArea1.setText("Calculating by " + cl.returnName());

        if (cl.returnRadio().equals("Sum")) {
            int sum = 0;
            for (int i = 0; i < listnumber.size(); i++) {
                sum += listnumber.get(i);
            }
            jTextArea1.append("\nSum = " + sum);

        }
        if (cl.returnRadio().equals("Max")) {
            int max = listnumber.get(0);
            for (int i = 1; i < listnumber.size(); i++) {
                if (max < listnumber.get(i)) {
                    max = listnumber.get(i);
                }
            }
            jTextArea1.append("\nMax = " + max);

        }
        if (cl.returnRadio().equals("Min")) {
            int min = listnumber.get(0);
            for (int i = 1; i < listnumber.size(); i++) {
                if (min > listnumber.get(i)) {
                    min = listnumber.get(i);
                }
            }

            jTextArea1.append("\nMin = " + min);

        }

    }//GEN-LAST:event_jMenuItem1ActionPerformed

