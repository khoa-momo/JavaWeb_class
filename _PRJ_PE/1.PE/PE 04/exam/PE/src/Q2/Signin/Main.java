    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        // TODO add your handling code here:
        SignIn s = new SignIn(this, true);
        s.setVisible(true);
        
        jTextField1.setText(s.returnName().toUpperCase());
        jTextArea1.append("User name = " + s.returnUserName() + "\n");
        jTextArea1.append("Password = " + s.returnPassword() + "\n");
        jTextArea1.append("Gender = " + s.returnGender() + "\n");
        jTextArea1.append("Country = " + s.returnCountry() + "\n");
        jTextArea1.append("Position = " + s.returnPosition() + "\n");
        if (s.check()) {
            DateFormat time = new SimpleDateFormat("hh:mm:ss EEE MMMMM d, yyyy");
            String outdate = time.format(s.returnDate());
            
            jTextArea1.append("Date created: " + outdate + "\n");
        }
        

    }//GEN-LAST:event_jMenuItem1ActionPerformed

