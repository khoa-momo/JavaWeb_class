    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        FileDialog fl = new FileDialog(this, true);
        fl.setVisible(true);

        //
        String pathfile = fl.returnPathfile();
        boolean isShowsize = fl.isShowsize();
        boolean isShowDate = fl.isShowDate();

        //Tao một đối tượng file:
        File file = new File(pathfile);
        if (!file.exists()) {
            JOptionPane.showConfirmDialog(this, "File path is wrong !");
        } else {
            String text = "";
            if (isShowsize) {
                text += "Size of file: " + getSizeOfFile(file) + " bytes \n";

            }
            if (isShowDate) {
                text += "Date: " + getDate() + "\n";
            }
            try {
                text += getContentOfFile(file);
            } catch (IOException ex) {
                Logger.getLogger(Parent.class.getName()).log(Level.SEVERE, null, ex);
            }
            jTextArea1.setText(text);
        }


    }//GEN-LAST:event_jMenuItem1ActionPerformed

