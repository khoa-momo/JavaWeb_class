    public String getSizeOfFile(File file) {
        String size = String.valueOf(file.length());
        return size;
    }

    public String getDate() {
        String currdate = null;

        //Lấy ngày tháng hiện tại
        Date date = Calendar.getInstance().getTime();

        //Formart ngay thang
        SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");
        currdate = sdf.format(date);
        return currdate;
    }

    public String getContentOfFile(File file) throws FileNotFoundException, IOException {
        String content = null;

        FileInputStream fis = new FileInputStream(file);
        LineInputStream lis = new LineInputStream(fis);
        String line = null;

        //Doc tung dong 
        while ((line = lis.readLine()) != null) {
            content += line + "\n";
        }
        if(fis!=null) fis.close();
        if(lis!=null) lis.close();
        return content;
    }
