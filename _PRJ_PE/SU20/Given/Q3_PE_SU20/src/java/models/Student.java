package models;

import java.time.LocalDate;

/**
 *
 * @author DELL
 */
public class Student {
    private int id;
    private String name;
    private LocalDate dob;
    private String gender; 
    
    public Student() {
    }

    public Student(int id, String name, LocalDate dob, String gender) {
        this.id = id;
        this.name = name;
        this.dob = dob;
        this.gender = gender;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public LocalDate getDob() {
        return dob;
    }

    public void setDob(LocalDate dob) {
        this.dob = dob;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }
 
    
}
